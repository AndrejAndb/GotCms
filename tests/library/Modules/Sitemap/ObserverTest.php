namespace Modules\Sitemap;

use Gc\Event\StaticEventManager,
    Gc\Registry,
    Gc\Document\Model as DocumentModel,
    Gc\DocumentType\Model as DocumentTypeModel,
    Gc\Layout\Model as LayoutModel,
    Gc\User\Model as UserModel,
    Gc\View\Model as ViewModel;
/**
 * Generated by PHPUnit_SkeletonGenerator 1.2.0 on 2013-01-12 at 13:33:24.
 *
 * @backupGlobals disabled
 * @backupStaticAttributes disabled
 * @group Modules
 * @category Gc_Tests
 * @package  Modules
 */
class ObserverTest extends \PHPUnit_Framework_TestCase
{
    /**
     * @var Observer
     */
    protected $_object;

    /**
     * @var Model
     */
    protected $_document;

    /**
     * @var ViewModel
     */
    protected $_view;

    /**
     * @var LayoutModel
     */
    protected $_layout;

    /**
     * @var UserModel
     */
    protected $_user;

    /**
     * @var DocumentTypeModel
     */
    protected $_documentType;

    /**
     * Sets up the fixture, for example, opens a network connection.
     * This method is called before a test is executed.
     */
    protected function setUp()
    {
        $this->_view = ViewModel::fromArray(array(
            'name' => 'View Name',
            'identifier' => 'View identifier',
            'description' => 'View Description',
            'content' => 'View Content'
        ));
        $this->_view->save();

        $this->_layout = LayoutModel::fromArray(array(
            'name' => 'Layout Name',
            'identifier' => 'Layout identifier',
            'description' => 'Layout Description',
            'content' => 'Layout Content'
        ));
        $this->_layout->save();

        $this->_user = UserModel::fromArray(array(
            'lastname' => 'User test',
            'firstname' => 'User test',
            'email' => 'test@test.com',
            'login' => 'test',
            'user_acl_role_id' => 1,
        ));

        $this->_user->setPassword('test');
        $this->_user->save();

        $this->_documentType = DocumentTypeModel::fromArray(array(
            'name' => 'Document Type Name',
            'description' => 'Document Type description',
            'icon_id' => 1,
            'default_view_id' => $this->_view->getId(),
            'user_id' => $this->_user->getId(),
        ));

        $this->_documentType->save();

        $this->_document = DocumentModel::fromArray(array(
            'name' => 'Document name',
            'url_key' => 'url-key',
            'status' => DocumentModel::STATUS_ENABLE,
            'show_in_nav' => TRUE,
            'user_id' => $this->_user->getId(),
            'document_type_id' => $this->_documentType->getId(),
            'view_id' => $this->_view->getId(),
            'layout_id' => $this->_layout->getId(),
            'parent_id' => NULL
        ));

        $this->_document->save();
        $this->_object = new Observer;
    }

    /**
     * Tears down the fixture, for example, closes a network connection.
     * This method is called after a test is executed.
     */
    protected function tearDown()
    {
        $this->_document->delete();
        $this->_documentType->delete();
        $this->_user->delete();
        $this->_layout->delete();
        $this->_view->delete();
        unset($this->_document);
        unset($this->_documentType);
        unset($this->_user);
        unset($this->_layout);
        unset($this->_view);
        unset($this->_object);
        StaticEventManager::resetInstance();
    }

    /**
     * @covers Modules\Sitemap\Observer::init
     */
    public function testInit()
    {
        $this->assertNull($this->_object->init());
    }

    /**
     * @covers Modules\Sitemap\Observer::addElement
     */
    public function testAddElement()
    {
        $mvc_event = Registry::get('Application')->getMvcEvent();
        $mvc_event->setParam('object', $this->_document);
        $this->_document->setUrlKey('new-url-key');
        $this->assertNull($this->_object->addElement($mvc_event));
        $this->assertNull($this->_object->addElement($mvc_event));
    }

    /**
     * @covers Modules\Sitemap\Observer::removeElement
     */
    public function testRemoveElement()
    {
        $mvc_event = Registry::get('Application')->getMvcEvent();
        $mvc_event->setParam('object', $this->_document);
        $this->assertNull($this->_object->removeElement($mvc_event));
    }
}
