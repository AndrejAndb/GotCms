namespace Modules\Blog\Controller;

use Modules\Blog\Bootstrap,
    Gc\Document\Model as DocumentModel,
    Gc\DocumentType\Model as DocumentTypeModel,
    Gc\Layout\Model as LayoutModel,
    Gc\User\Model as UserModel,
    Gc\View\Model as ViewModel,
    Gc\Registry;

/**
 * Generated by PHPUnit_SkeletonGenerator 1.2.0 on 2012-12-06 at 14:00:49.
 *
 * @backupGlobals disabled
 * @backupStaticAttributes disabled
 * @group Modules
 * @category Gc_Tests
 * @package  Modules
 */
class IndexControllerTest extends \PHPUnit_Framework_TestCase
{
    /**
     * @var IndexController
     */
    protected $_object;

    /**
     * @var Bootstrap
     */
    protected $_boostrap;

    /**
     * @var Model
     */
    protected $_document;

    /**
     * @var ViewModel
     */
    protected $_view;

    /**
     * @var LayoutModel
     */
    protected $_layout;

    /**
     * @var UserModel
     */
    protected $_user;

    /**
     * @var DocumentTypeModel
     */
    protected $_documentType;

    /**
     * Sets up the fixture, for example, opens a network connection.
     * This method is called before a test is executed.
     */
    protected function setUp()
    {
        $this->_view = ViewModel::fromArray(array(
            'name' => 'View Name',
            'identifier' => 'View identifier',
            'description' => 'View Description',
            'content' => 'View Content'
        ));
        $this->_view->save();

        $this->_layout = LayoutModel::fromArray(array(
            'name' => 'Layout Name',
            'identifier' => 'Layout identifier',
            'description' => 'Layout Description',
            'content' => 'Layout Content'
        ));
        $this->_layout->save();

        $this->_user = UserModel::fromArray(array(
            'lastname' => 'User test',
            'firstname' => 'User test',
            'email' => 'test@test.com',
            'login' => 'test',
            'user_acl_role_id' => 1,
        ));

        $this->_user->setPassword('test');
        $this->_user->save();

        $this->_documentType = DocumentTypeModel::fromArray(array(
            'name' => 'Document Type Name',
            'description' => 'Document Type description',
            'icon_id' => 1,
            'default_view_id' => $this->_view->getId(),
            'user_id' => $this->_user->getId(),
        ));

        $this->_documentType->save();

        $this->_document = DocumentModel::fromArray(array(
            'name' => 'Document name',
            'url_key' => 'url-key',
            'status' => DocumentModel::STATUS_ENABLE,
            'show_in_nav' => TRUE,
            'user_id' => $this->_user->getId(),
            'document_type_id' => $this->_documentType->getId(),
            'view_id' => $this->_view->getId(),
            'layout_id' => $this->_layout->getId(),
            'parent_id' => NULL
        ));

        $this->_document->save();

        $this->_boostrap = new Bootstrap();
        $this->_boostrap->install();
        $this->_object = new IndexController(Registry::get('Application')->getRequest(), Registry::get('Application')->getResponse());
    }

    /**
     * Tears down the fixture, for example, closes a network connection.
     * This method is called after a test is executed.
     */
    protected function tearDown()
    {
        $this->_boostrap->uninstall();
        $this->_document->delete();
        $this->_view->delete();
        $this->_layout->delete();
        $this->_documentType->delete();
        $this->_user->delete();
        unset($this->_document);
        unset($this->_object);
        unset($this->_view);
        unset($this->_layout);
        unset($this->_documentType);
        unset($this->_user);
    }

    /**
     * @covers Modules\Blog\Controller\IndexController::indexAction
     */
    public function testIndexAction()
    {
        $this->assertInternalType('array', $this->_object->indexAction());
    }

    /**
     * @covers Modules\Blog\Controller\IndexController::documentCommentAction
     */
    public function testDocumentCommentActionWithRedirect()
    {
        $this->setExpectedException('Zend\Mvc\Exception\DomainException');
        $this->assertInternalType('array', $this->_object->documentCommentAction());
    }

    /**
     * @covers Modules\Blog\Controller\IndexController::documentCommentAction
     */
    public function testDocumentComment()
    {
        Registry::get('Application')->getRequest()->getQuery()->set('id', $this->_document->getId());
        $this->assertInternalType('array', $this->_object->documentCommentAction());
    }

    /**
     * @covers Modules\Blog\Controller\IndexController::documentCommentAction
     */
    public function testDocumentCommentWithPostData()
    {
        $this->setExpectedException('Zend\Mvc\Exception\DomainException');
        $request = Registry::get('Application')->getRequest();
        $request->setMethod('POST');
        $request->getQuery()->set('id', $this->_document->getId());

        $post = $request->getPost();

        $post->set('comment', array(
            '1' => array(
                'delete' => TRUE
            ),
            '2' => array(
                'message' => TRUE,
                'wrong-name' => FALSE
            ),
        ));

        $this->assertNUll($this->_object->documentCommentAction());

        $request->setMethod('GET');
    }
}
